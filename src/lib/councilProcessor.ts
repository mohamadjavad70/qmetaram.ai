export function processWithCouncil(problem: string): CouncilProcessingResult {
  const startTime = Date.now();

  // Stage 1: 9 aspects from each of the 31 members (excluding commander in stage 1)
  const activeMembers = councilMembers.filter((m) => m.id !== 32);
  const stage1: MemberPerspective[] = activeMembers.map((member) => ({
    member,
    aspects: generateMemberAspects(member, problem),
  }));

  // Stage 2: 6 structured answers
  const stage2 = generateStructuredAnswers(stage1, problem);

  // Stage 3: Filter answers
  const stage3 = filterAnswers(stage2);

  // Stage 4: Final answer from expert programmer + commander
  const stage4 = generateFinalAnswer(problem, stage3, startTime);

  return {
    problem,
    stage1,
    stage2,
    stage3,
    stage4,
    timestamp: new Date(),
  };
}
