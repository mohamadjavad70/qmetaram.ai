import { CouncilMember, councilMembers } from "./councilMembers";

export interface MemberPerspective {
  member: CouncilMember;
  aspects: string[]; // 9 aspects
}

export interface StructuredAnswer {
  id: number;
  title: string;
  description: string;
  agreementScore: number; // percentage of members who agree
  supportingMembers: string[];
  isEconomic: boolean;
  isSecurity: boolean;
  isEthical: boolean;
}

export interface FilteredAnswer extends StructuredAnswer {
  passesFilter: boolean;
  filterReasons: string[];
}

export interface FinalAnswer {
  executiveDirective: string;
  implementationSteps: string[];
  technicalCommands?: string[];
  commanderApproval: string;
  processingTime: number;
}

export interface CouncilProcessingResult {
  problem: string;
  stage1: MemberPerspective[];
  stage2: StructuredAnswer[];
  stage3: FilteredAnswer[];
  stage4: FinalAnswer;
  timestamp: Date;
}

